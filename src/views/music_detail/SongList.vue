<!-- 歌单详情，歌曲列表 -->
<template>
  <div>
    <div class="h-28 bg-red-400"></div>
    <van-list v-model="loading" :finished="finished" finished-text="--底线♪(^∇^*)--" @load="onLoad">
      <van-cell v-for="item in list" :key="item" :title="item" />
    </van-list>
  </div>
</template>
<script>
export default {
  name: 'songList',
  data() {
    return {
      list: [],
      loading: false,
      finished: false
    }
  },
  mounted() {},
  methods: {
    onLoad() {
      setTimeout(() => {
        for (let i = 0; i < 10; i++) {
          this.list.push(this.list.length + 1)
        }

        // 加载状态结束
        this.loading = false

        // 数据全部加载完成
        if (this.list.length >= 40) {
          this.finished = true
        }
      }, 1000)
    }
  }
}
</script>
